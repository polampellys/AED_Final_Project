    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        int driverrow = jTable2.getSelectedRow();
        
        if(row == -1)
        {
            JOptionPane.showMessageDialog(null,"Please select a Patient", "Select Warning", 2);
            
        }
        
        if(driverrow == -1){
            JOptionPane.showMessageDialog(null,"Please select a Ambulance", "Select Warning", 2);
        }
        
        Patient patient = applicationSystem.getPatientUserDirectory().getPatientDirectory().getPatientlist().get(row);
        AssignedVehicles av = applicationSystem.getAmbulanceUserAccountDirectory().getAssignedVehiclesDirectory().getAssignedVehicles().get(driverrow);
        Driver driver = av.getDriver();
        
        if(!av.getStatus().equals("Avaialable")){
            JOptionPane.showMessageDialog(null, "Requested Ambulance is not availble");
            return;
        }
        av.setStatus("On Call");
        AmbulanceRequest ar = applicationSystem.getPatientUserDirectory().getAmbulanceRequestDirectory().requestAmbulance(receptionist, patient, av,jTextField1.getText());
        
        driver.getAmbulanceRequestDirectory().getAmbulanceRequestList().add(ar);
        
        populateAmbulance();
    }//GEN-LAST:event_jButton1ActionPerformed


