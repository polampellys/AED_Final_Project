    private class MyCellRenderer extends DefaultTableCellRenderer {
        public Component getTableCellRendererComponent(JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column) {
            Component c = super.getTableCellRendererComponent(table, value, isSelected, hasFocus, row, column);
            if (row % 2 == 0) {
                c.setBackground(new Color(0x99, 0xcc, 0xcc));
            } else {
                c.setBackground(new Color(0x3D, 0x76, 0x7D));
            }
            return c;
        }
    }
    
    public void PopulateTable(){
        PatientTableModel.setRowCount(0);
        
        for(Patient patient : applicationSystem.getPatientUserDirectory().getPatientDirectory().getPatientlist()){
            Object rows[] = new Object[1];
            rows[0] = patient.getUsername();
            
            
            PatientTableModel.addRow(rows);
        }
    }
    
    public void populateAmbulance(){
        AmbulanceTableModel.setRowCount(0);
        
        for(AssignedVehicles av : applicationSystem.getAmbulanceUserAccountDirectory().getAssignedVehiclesDirectory().getAssignedVehicles()){
            Object rows[] = new Object[4];
            rows[0] = av.getVehicle().getNumber();
            rows[1] = av.getVehicle().getName();
            rows[2] = av.getDriver().getUsername();
            rows[3] = av.getStatus();
            
            
            AmbulanceTableModel.addRow(rows);
        }
    }

